"""Operator descriptor protocol."""

from typing import Protocol

__all__ = ["Op"]


class Op(Protocol):
    """
    Operator descriptor protocol (stateless, reusable).

    MathExpr holds a reference to an Op when it represents an operator
    application (i.e., an OpNode).
    """

    @property
    def name(self) -> str: ...

    @property
    def arity(self) -> int: ...

