"""Data types."""

from enum import Enum

__all__ = ["DType"]


class DType(Enum):
    """
    Scalar/element dtypes supported by PixelPrism's math system.

    Extend as needed; backends may map these to their native types.
    """

    FLOAT32 = "float32"
    FLOAT64 = "float64"
    INT32 = "int32"
    INT64 = "int64"
    BOOL = "bool"

